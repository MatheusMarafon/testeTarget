# a) 1, 3, 5, 7, ...
def proximo_a():
    return 9  # Sequência de números ímpares consecutivos

# b) 2, 4, 8, 16, 32, 64, ...


def proximo_b():
    return 128  # Potências de 2

# c) 0, 1, 4, 9, 16, 25, 36, ...


def proximo_c():
    return 49  # Quadrados perfeitos

# d) 4, 16, 36, 64, ...


def proximo_d():
    return 100  # Quadrados perfeitos pares

# e) 1, 1, 2, 3, 5, 8, ...


def proximo_e():
    return 13  # Sequência de Fibonacci

# f) 2, 10, 12, 16, 17, 18, 19, ...


def proximo_f():
    return 20  # Sem sequência aparente


# Impressão dos resultados
print(f"Próximo elemento de cada sequência:")
print(f"a) {proximo_a()}")
print(f"b) {proximo_b()}")
print(f"c) {proximo_c()}")
print(f"d) {proximo_d()}")
print(f"e) {proximo_e()}")
print(f"f) {proximo_f()}")
